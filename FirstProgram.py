#Arithmetic operators

a = 5
b = 2

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)



#Relational Operators
a = 50
b = 20

print(a == b)  #False
print(a != b)  #True
print(a >= b)  #True
print( a > b)  #True
print(a <= b)  #false


#Assignment Operator

