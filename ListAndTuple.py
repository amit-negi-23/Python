# List Method

num = [2, 1, 3]

print(num)

# lis.append()
num.append(4)
print(num)

# lis.sort()
num.sort()
print(num)

# lis.sort(reverse = True)
num.sort(reverse=True)
print(num)



alpha = ['a', 'b', 'c', 'd', 'e', 'f']

# lis.reverse()
alpha.reverse()
print(alpha)




digi = [2, 1, 3, 1]

# lis.insert(idx, el)
digi.insert(1, 5)
print(digi)

# lis.remove(el)
digi.remove(1)
print(digi)

# lis.pop(idx)
digi.pop(0)
print(digi)